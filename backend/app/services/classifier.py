# просто "Клуб по интересам" тоже считались бы плохими. Теперь требуем,
# чтобы оба слова стояли рядом.

# Корни слов-исключений (стоп-лист) — их присутствие делает объект unknown.
# ВАЖНО: этот список проверяется ПЕРВЫМ, поэтому в него нельзя добавлять
# слова, которые пересекаются с настоящими "хорошими" объектами — раньше
# здесь были "спорт", "фитнес", "бассейн", "детск", "игров", из-за чего
# ЛЮБАЯ школа, детсад, фитнес-центр или бассейн автоматически становились
# unknown ещё до проверки GOOD_ROOTS. Ниже это исправлено: вместо блокировки
# целых категорий исключаются только конкретные типы магазинов/услуг.
