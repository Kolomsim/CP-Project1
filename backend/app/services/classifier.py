"""
Классификация объекта (хороший/плохой/неизвестный) при поиске окружения дома.

КЛЮЧЕВОЙ ПРИНЦИП: классифицируем ТОЛЬКО по официальным рубрикам 2ГИС
(item['rubrics']), а НЕ по свободному названию организации (item['name']).

Название — рекламный текст и может быть каким угодно: строительная фирма
"ЖД-Строй", кофейня "Клубничка", магазин "Депо джинсов" и т.п. Поиск по
названию неизбежно даёт ложные совпадения (это и была причина, почему
в выдаче попадались не сами объекты, а компании с похожим словом в
названии). Рубрика — категория, которую 2ГИС сама присвоила объекту по
своему справочнику, поэтому она значительно надёжнее.

Для максимальной точности лучший вариант на будущее — искать сразу по
rubric_id прямо в запросе к API (см. dgis.fetch_nearby_dgis(rubric_id=...)),
тогда этот модуль становится вторым, подстраховочным слоем проверки,
а не единственным источником истины.
"""

