# ==================== КОРНИ ДЛЯ РЕГУЛЯРОК ====================
# Корень + ОГРАНИЧЕННОЕ число букв после него (см. _build_pattern) —
# это ловит склонения ("школа"/"школы"/"школе"), но не любые слова,
# которые случайно начинаются с тех же букв.
#
# Раньше продолжение после корня было неограниченным ([а-яё]*), поэтому,
# например, короткий корень "жд" ловил вообще любое слово, начинающееся
# с "жд" (включая обычные слова и название стройфирмы), а корень "клуб"
# ловил бы "клубника" целиком. Ограничение длины продолжения — это и есть
# основной технический фикс "точного совпадения".

GOOD_ROOTS = [
    "школ", "гимнази", "лице",
    "детск", "сад",
    "площадк",                          # детская / игровая площадка
    "парк", "сквер", "бульвар", "набережн",
    "спорт", "фитнес", "бассейн", "стадион",
    "поликлиник", "больниц", "аптек", "медцентр",
    "библиотек", "музе", "театр", "кинотеатр",
    "метро", "мцд", "мцк",
    "остановк", "автобус", "трамва",
    "тц", "супермаркет", "продуктов",
]

BAD_ROOTS = [
    "завод", "фабрик", "комбинат", "тэц", "котельн", "очистн",
    "свалк", "полигон",
    "бар",
    "железнодорож", "вокзал", "депо",
    "кладбищ", "аэропорт",
]
